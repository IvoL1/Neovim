-- init.lua básico mínimo

vim.opt.number = true          -- mostra número das linhas

vim.opt.relativenumber = true  -- linhas relativas

vim.opt.expandtab = true       -- usa espaços no lugar de tab

vim.opt.shiftwidth = 4         -- tamanho da indentação

vim.opt.tabstop = 4            -- tab = 4 espaços

vim.opt.cursorline = true      -- destaca linha do cursor

vim.opt.wrap = false           -- não quebra linha visualmente

vim.opt.ignorecase = true      -- busca case-insensitive

vim.opt.smartcase = true       -- busca case-sensitive se usar maiúscula

vim.opt.clipboard = "unnamedplus" -- usa clipboard do sistema

